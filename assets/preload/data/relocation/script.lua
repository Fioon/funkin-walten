function onBeatHit()
   if curBeat == 112 then
      setProperty('defaultCamZoom', 0.9);
   end
   if curBeat == 144 then
      setProperty('defaultCamZoom', 0.8);
   end
   if curBeat == 204 then
      setProperty('defaultCamZoom', 1.0);
   end
   if curBeat == 208 then
      setProperty('defaultCamZoom', 0.9);
   end
end